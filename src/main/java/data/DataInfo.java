package data;

public class DataInfo {

    private String valueX;
    private String valueY;
    private String valueR;

    public DataInfo(String valueX, String valueY, String valueR) {
        this.valueY = valueY;
        this.valueX = valueX;
        this.valueR = valueR;
    }

    public String getValueX() {
        return valueX;
    }

    public String getValueY() {
        return valueY;
    }

    public String getValueR() {
        return valueR;
    }
}
